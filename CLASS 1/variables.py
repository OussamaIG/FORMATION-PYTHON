#Variables types
# Int - Integer
a = 15
print(a)
# Float - Floating point number
a = 15.5
print(a)
# Char - Character (single character string)
a = 'a'
print(a)
# String - Sequence of characters
a = 'Hello World'
print(a)
# Boolean - True or False
a = True
print(a)
a = False
print(a)

