x = input('Give me a number: ')
y = input('Give me another number: ')
# Mathematical operations
# Addition  
r = int(x) + int(y)
print('Addition:', r)
# Subtraction
r = int(x) - int(y)
print('Subtraction:', r)
# Multiplication
r = int(x) * int(y)     
print('Multiplication:', r)
# Division
r = int(x) / int(y)
print('Division:', r)   
# Modulus
r = int(x) % int(y)
print('Modulus:', r)